    private void btSalvarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarClienteActionPerformed
        // TODO add your handling code here:
        imprimir("Salvar Cliente Novo");
        
        int _id = 0;
        String _nome = jTextFieldNome.getText();
        String _usuario = jTextUsuario.getText();
        String _senha = jTextSenha.getText();
        String _cpf = jTextCPF.getText();
        Endereco _endereco = new Endereco();
        _endereco.setRua(jTextEnderecoRua.getText());
        String _telefone = jTextTelefone.getText();
        String _email = jTextEmail.getText();
        String _nascimento = jTextNascimento.getText();
        
        if(jComboListaClientes.getSelectedItem().equals("Adicionar: Novo Cliente")){
            // próximo Id
            for (Cliente clienteFromLista : HomeController.listaClientes) {
                if(_id <= clienteFromLista.getId()){
                    _id = clienteFromLista.getId()+1;
                }
            }
            Cliente cliente = new Cliente(_id, _nome, _usuario, _senha, _cpf, _endereco, _telefone, _email, _nascimento);
            System.out.println(cliente.getId()+","+cliente.getNome());
            HomeController.addCliente2Lista(cliente);
        }
        else{
            Cliente cliente = new Cliente(_id, _nome, _usuario, _senha, _cpf, _endereco, _telefone, _email, _nascimento);
            HomeController.updateCliente2Lista(cliente);
        }
        
        this.setVisible(false);
        
    }//GEN-LAST:event_btSalvarClienteActionPerformed

