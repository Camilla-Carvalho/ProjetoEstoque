    public void carregarDados() {

        HomeController.print("carregarDadosClientes() in CadastroClientes");

        this.jComboListaClientes = new javax.swing.JComboBox<>();
        this.jComboListaClientes.addItem("Adicionar: Novo Cliente");
        this.jComboListaClientes.updateUI();
        
        File file = new File("projetoestoque.clientes.persistencia.db");
        
        ObjectInputStream in = null;
        java.util.List<Cliente> listaClientesTmp = null;
        
        try {
            in = new ObjectInputStream(new FileInputStream(file));
            listaClientesTmp = (ArrayList<Cliente>) in.readObject();
            //listaCliente = listaClientesTmp;
            in.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(HomeController.class.getName()).log(Level.SEVERE, null, ex.getMessage());
        } catch (IOException ex) {
            Logger.getLogger(HomeController.class.getName()).log(Level.SEVERE, null, ex.getMessage());
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(HomeController.class.getName()).log(Level.SEVERE, null, ex.getMessage());
        }
        
        if(listaClientesTmp==null){
            listaClientesTmp = new ArrayList<Cliente>();
        }
        
        listaClientesCadastrados = listaClientesTmp;
        
        this.jComboListaClientes.removeAllItems();
        this.jComboListaClientes.addItem("Adicionar: Novo Cliente");
        
        for (Cliente cliente : listaClientesTmp) {
            HomeController.print("listaClientesTmp: "+cliente.getNome());
            this.jComboListaClientes.addItem(cliente.getNome());
        }
    }
    
    
