    private void jComboListaFornecedoresActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboListaFornecedoresActionPerformed
        if(evt.getActionCommand().equals("comboBoxChanged")){
            if(jComboListaFornecedores.getSelectedItem()==null || jComboListaFornecedores.getSelectedItem().equals("Adicionar: Novo Usuário")){
                //this.jLabelUsuarioId.setText("0");
                this.jLabelUsuarioId.setText("");
                this.jTextFieldNome.setText("");
                this.jTextFieldLogin.setText("");
                this.jPasswordFieldSenha.setText("");
                this.jTextCNPJ.setText("");
                this.jTextSetor.setText("");
            }
            else{
                for (Fornecedor fornecedorCadastrado : listaForcedoresCadastrados) {
                    if(fornecedorCadastrado.getNome().equalsIgnoreCase(jComboListaFornecedores.getSelectedItem().toString())){
                        //System.out.println("comboBoxChanged selected: " + jComboListaUsuarios.getSelectedItem() + ", " + fornecedorCadastrado.getNome());
                        this.jLabelUsuarioId.setText(""+fornecedorCadastrado.getId());
                        this.jTextFieldNome.setText(fornecedorCadastrado.getNome());
                        this.jTextFieldLogin.setText(fornecedorCadastrado.getLogin());
                        this.jPasswordFieldSenha.setText(fornecedorCadastrado.getSenha());
                        this.jTextCNPJ.setText(fornecedorCadastrado.getCnpj());
                        this.jTextSetor.setText(fornecedorCadastrado.getSetorAtuacao());
                    }
                }
            }
            
        }
    }//GEN-LAST:event_jComboListaFornecedoresActionPerformed

