    public void carregarDados() {

        HomeController.print("carregarDadosUsuarios() in CadastroUsuarios");

        //this.jComboListaUsuarios = new javax.swing.JComboBox<>();

        File file = new File("projetoestoque.fornecedor.persistencia.db");
        
        ObjectInputStream in = null;
        java.util.List<Fornecedor> listaFornecedoresTmp = null;
        
        try {
            in = new ObjectInputStream(new FileInputStream(file));
            listaFornecedoresTmp = (ArrayList<Fornecedor>) in.readObject();
            //listaUsuario = listaUsuariosTmp;
            in.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(HomeController.class.getName()).log(Level.SEVERE, null, ex.getMessage());
        } catch (IOException ex) {
            Logger.getLogger(HomeController.class.getName()).log(Level.SEVERE, null, ex.getMessage());
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(HomeController.class.getName()).log(Level.SEVERE, null, ex.getMessage());
        }
        
        if(listaFornecedoresTmp==null){
            listaFornecedoresTmp = new ArrayList<Fornecedor>();
        }
        
        listaForcedoresCadastrados = listaFornecedoresTmp;
        
        this.jComboListaFornecedores.removeAllItems();
        this.jComboListaFornecedores.addItem("Adicionar: Novo Fornecedor");
        
        for (Fornecedor fornecedor : listaFornecedoresTmp) {
            this.jComboListaFornecedores.addItem(fornecedor.getNome());
        }
    }

