    public void carregarDados() {

        HomeController.print("carregarDadosProdutos() in CadastroProdutos");

        //this.jComboListaProdutos = new javax.swing.JComboBox<>();

        File file = new File("projetoestoque.fornecedor.persistencia.db");
        
        ObjectInputStream in = null;
        java.util.List<Produto> listaProdutosTmp = null;
        
        try {
            in = new ObjectInputStream(new FileInputStream(file));
            listaProdutosTmp = (ArrayList<Produto>) in.readObject();
            //listaProduto = listaProdutosTmp;
            in.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(HomeController.class.getName()).log(Level.SEVERE, null, ex.getMessage());
        } catch (IOException ex) {
            Logger.getLogger(HomeController.class.getName()).log(Level.SEVERE, null, ex.getMessage());
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(HomeController.class.getName()).log(Level.SEVERE, null, ex.getMessage());
        }
        
        if(listaProdutosTmp==null){
            listaProdutosTmp = new ArrayList<Produto>();
        }
        
        listaForcedoresCadastrados = listaProdutosTmp;
        
        this.jComboListaProdutos.removeAllItems();
        this.jComboListaProdutos.addItem("Adicionar: Novo Produto");
        
        for (Produto fornecedor : listaProdutosTmp) {
            this.jComboListaProdutos.addItem(fornecedor.getNome());
        }
    }

