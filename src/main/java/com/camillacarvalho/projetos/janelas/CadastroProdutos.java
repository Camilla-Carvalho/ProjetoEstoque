    private void jComboListaProdutosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboListaProdutosActionPerformed
        if(evt.getActionCommand().equals("comboBoxChanged")){
            if(jComboListaProdutos.getSelectedItem()==null || jComboListaProdutos.getSelectedItem().equals("Adicionar: Novo Usuário")){
                //this.jLabelProdutoId.setText("0");
                this.jLabelProdutoId.setText("");
                this.jTextFieldNome.setText("");
                this.jTextDescricao.setText("");
                this.jTextPreco.setText("");
            }
            else{
                for (Produto produtoCadastrado : listaForcedoresCadastrados) {
                    if(produtoCadastrado.getNome().equalsIgnoreCase(jComboListaProdutos.getSelectedItem().toString())){
                        //System.out.println("comboBoxChanged selected: " + jComboListaProdutos.getSelectedItem() + ", " + fornecedorCadastrado.getNome());
                        this.jLabelProdutoId.setText(""+produtoCadastrado.getId());
                        this.jTextFieldNome.setText(produtoCadastrado.getNome());
                        this.jTextDescricao.setText(produtoCadastrado.getDescricao());
                        this.jTextPreco.setText(produtoCadastrado.getPreco());
                    }
                }
            }
            
        }
    }//GEN-LAST:event_jComboListaProdutosActionPerformed

